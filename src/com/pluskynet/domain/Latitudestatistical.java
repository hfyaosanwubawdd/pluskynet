package com.pluskynet.domain;

public class Latitudestatistical {
	private int id;
	private int latitudeid;
	private String latitudename;
	private String latitudefname;
	private int latitudenums;
	private String sectionname;
	private String sectionnums;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getLatitudeid() {
		return latitudeid;
	}
	public void setLatitudeid(int latitudeid) {
		this.latitudeid = latitudeid;
	}
	public String getLatitudename() {
		return latitudename;
	}
	public void setLatitudename(String latitudename) {
		this.latitudename = latitudename;
	}
	public String getLatitudefname() {
		return latitudefname;
	}
	public void setLatitudefname(String latitudefname) {
		this.latitudefname = latitudefname;
	}
	public int getLatitudenums() {
		return latitudenums;
	}
	public void setLatitudenums(int latitudenums) {
		this.latitudenums = latitudenums;
	}
	public String getSectionname() {
		return sectionname;
	}
	public void setSectionname(String sectionname) {
		this.sectionname = sectionname;
	}
	public String getSectionnums() {
		return sectionnums;
	}
	public void setSectionnums(String sectionnums) {
		this.sectionnums = sectionnums;
	}
}
